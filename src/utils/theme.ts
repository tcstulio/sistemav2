/**
 * Get theme classes for a specific color
 * @param color - The theme color to get classes for
 * @returns ThemeClasses object with all pre-defined class combinations
 */
export function getThemeClasses(color: ThemeColor | string): ThemeClasses {
    const validColor = (color in themeClassMap) ? color as ThemeColor : 'indigo';
    return themeClassMap[validColor];
}

/**
 * Get a specific theme class
 * @param color - The theme color
 * @param property - The property to get (e.g., 'bg50', 'text600', 'primaryButton')
 * @returns The Tailwind class string
 */
export function getThemeClass(color: ThemeColor | string, property: keyof ThemeClasses): string {
    return getThemeClasses(color)[property];
}

/**
 * Helper to create conditional card classes based on selection state
 * @param color - The theme color
 * @param isSelected - Whether the card is selected
 * @returns The appropriate Tailwind classes
 */
export function getCardClasses(color: ThemeColor | string, isSelected: boolean): string {
    const theme = getThemeClasses(color);
    return isSelected ? theme.activeCard : theme.inactiveCard;
}

// Default export for convenience
export default {
    getThemeClasses,
    getThemeClass,
    getCardClasses,
};
